# Ввод числа от 1 до 7
m = int(input("Введите номер дня недели (1-7): "))

# Определение названия дня недели
if m == 1:
    day = "Понедельник"
elif m == 2:
    day = "Вторник"
elif m == 3:
    day = "Среда"
elif m == 4:
    day = "Четверг"
elif m == 5:
    day = "Пятница"
elif m == 6:
    day = "Суббота"
elif m == 7:
    day = "Воскресенье"
else:
    day = "Неверный ввод"

# Вывод результата
print("День недели:", day)
